class Programme
{
    constructor(code, name, faculty, faculty_code) {
        this.code = code;
        this.name = name;
        this.faculty = faculty;
        this.faculty_code = faculty_code;
    }
}

class Building
{
    constructor(code, name, campus_name) {
        this.code = code;
        this.name = name;
        this.campus_name = campus_name;
    }
}